#include "libft.h"

int			ft_atoi(const char *str)
{
	int		i;
	int		result;
	int		check;

	i = 0;
	result = 0;
	check = 1;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-')
		check = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - 48);
		i++;
	}
	result = result * check;
	return (result);
}
